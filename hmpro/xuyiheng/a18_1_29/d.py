#字符串运算符号

a = 'Hello'
b = 'Python'

print ("a + b 输出结果： ", a+b)
print ("a * 2 输出结果： ", a*2)
print ("a[1]输出结果： ", a[1])
print("a[1:4]输出结果： ", a[1:4])

if ("H" in a) :
    print ("H在变量a中")
else :
    print ("H不再变量a中")

if ("M" not in a) :
    print("M不再变量a中")
else :
    print("M在变量a中")

